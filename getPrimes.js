function getPrimes(){
	for (i = 2; i<100; i++){
		if (i % 2 === 0 & i !== 2){document.write();}
	    else if (i % 3 === 0 & i !== 3){document.write();}
	    else if (i % 5 === 0 & i !== 5){document.write();}
	    else if (i % 7 === 0 & i !== 7){document.write();}
	    else{document.write(i + " ");}
		
	}
}